const STORAGE_KEY = 'sidebar-collapsed';

const Sidebar = ({ collapsed, onToggle }) => {
  const location = useLocation();
  const { user } = useAuth();

  const isActive = (path) =>
    location.pathname === path || location.pathname.startsWith(path + '/');

  const items = NAV_BY_ROLE[user?.role] || NAV_BY_ROLE.user;

  return (
    <aside className={`dashboard-sidebar${collapsed ? ' sidebar-collapsed' : ''}`}>
      {/* Header: logo + toggle button */}
      <div className="sidebar-header">
        {!collapsed && (
          <Link to="/" className="sidebar-logo">AutoSphere</Link>
        )}
        <button
          className="sidebar-toggle-btn"
          onClick={onToggle}
          aria-label={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
          title={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
        >
          {collapsed ? '▶' : '◀'}
        </button>
      </div>

      {/* Nav items */}
      <div className="sidebar-content">
        <nav className="sidebar-nav">
          {/* Home link — always visible so users can return to the public site */}
          <Link
            to="/"
            className="sidebar-item"
            title={collapsed ? 'Home' : undefined}
          >
            <span className="sidebar-icon"><Icon name="home" /></span>
            {!collapsed && <span className="sidebar-label">Home</span>}
          </Link>

          {/* Role-specific nav items */}
          {items.map((item) => (
            <Link
              key={item.path}
              to={item.path}
              className={`sidebar-item ${isActive(item.path) ? 'active' : ''}`}
              title={collapsed ? item.label : undefined}
            >
              <span className="sidebar-icon"><Icon name={item.icon} /></span>
              {!collapsed && (
                <span className="sidebar-label">{item.label}</span>
              )}
            </Link>
          ))}
        </nav>
      </div>
    </aside>
  );
};

export default Sidebar;
